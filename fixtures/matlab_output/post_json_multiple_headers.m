%% Web Access using Data Import and Export API
uri = 'https://0.0.0.0/rest/login-sessions';
body = struct(...
    'userName', 'username123',...
    'password', 'password123',...
    'authLoginDomain', 'local'...
);
options = weboptions(...
    'MediaType', 'application/json',...
    'HeaderFields', {'X-API-Version' '200'}...
);
response = webwrite(uri, body, options);

%% HTTP Interface
import matlab.net.*
import matlab.net.http.*
import matlab.net.http.io.*

header = [
    HeaderField('Content-Type', 'application/json')
    HeaderField('X-API-Version', '200')
]';
uri = URI('https://0.0.0.0/rest/login-sessions');
body = JSONProvider(struct(...
    'userName', 'username123',...
    'password', 'password123',...
    'authLoginDomain', 'local'...
));
response = RequestMessage('post', header, body).send(uri.EncodedURI);
